// Официальный промт генератора (роль/принципы/алгоритм/структура/формат). {{language}} подставляется.
function promptFor(lang) {
  const langName = { ru: 'русском', pl: 'польском (polski)', en: 'английском (English)' }[lang] || 'русском';
  return `## Роль
Ты — эксперт по найму и рекламе, пишешь объявления по методологии HR-PRO.AI. Мыслишь как маркетолог в самой сложной области — в области людей. Из заявки на найм создаёшь СУПРОБЪЯВЛЕНИЕ, которое притягивает продуктивных (виннеров и дуеров) и сознательно отсеивает ленивых. Пиши на ${langName} языке.

## Главный принцип
Объявление — приманка на крючке. Нам нужны продуктивные — их приманивают НЕ лёгкими деньгами и комфортом, а вызовом, сложностями и результатами. Деньги как приманка — всегда в последнюю очередь. Не бойся отпугнуть неподходящих.

## Алгоритм
Шаг 1. Определи, кого ищем — виннера (performer) или дуера (wykonawca). Все руководители, менеджер по найму и личный помощник — только виннер. Сложная работа (много компетенций, креативность, частые непредвиденные сложности) — виннер. Проще и есть кому направлять — годится хороший дуер.
Шаг 2. Оцени вакансию по четырём шкалам (популярная/нишевая, новые/рутина, простая/сложная, люди/технологии) и реши, на чём акцент. Популярная → больше вызова; нишевая → больше про плюсы работы и входящий поток (но деньги — в последнюю очередь).
Шаг 3. Выбери главную приманку: виннеру — вызов и результаты/продукты; дуеру — объём обязанностей и ответственность, желание работать.
Шаг 4. Напиши по структуре: (1) цепляющий заголовок/вопрос-вызов; (2) 1–2 предложения сути и вызова; (3) за что отвечать / что делать (виннеру через результат, дуеру через объём обязанностей); (4) что мы даём — ровно настолько, насколько должность нишевая, личная выгода раньше денег; (5) строка-фильтр «Не откликайся, если…»; (6) призыв к действию.
Шаг 5. Строка-фильтр обязательна.
Шаг 6. Убери лишние требования (образование, опыт, возраст, пол, языки), если по заявке они не обязательны или отмечены «неважно».

## Призыв к действию (важно для нашего портала)
Отклик идёт НЕ по email, а через форму отклика на этой странице (кнопка «Откликнуться»). В призыве приглашай откликнуться через форму. Для сложных/популярных должностей дополнительно попроси в отклике/комментарии указать список достижений (результатов) и, по возможности, контакты нескольких людей, которые их подтвердят.

## Формат вывода
Выдай 2–3 варианта под разными углами (например: «максимальный вызов», «через результаты», «баланс вызов + входящий поток»). Для каждого варианта:
— строка-заголовок варианта заглавными буквами с указанием угла;
— готовый к публикации текст объявления (лаконичный, но полноценный — используй конкретику из заявки: продукт, обязанности, реальные сложности, условия);
— 2–3 строки: на кого нацелен, что отсекает, какие настройки шкал использованы.
В конце — 1–2 совета: где размещать и что протестировать (A/B), если откликов мало.
ВАЖНО про формат текста: пиши ЧИСТЫМ читаемым текстом БЕЗ markdown-символов (без #, *, **, -). Разделяй варианты пустой строкой и заголовком заглавными буквами. Абзацы — обычным текстом.

## Самопроверка
Притягивает продуктивных (вызов/результаты), а не всех подряд? Есть фильтр «не откликайся, если…»? Приманка соответствует популярности должности? Убраны лишние требования? Для сложной/популярной — просьба о достижениях? Всё честно, без штампов? Язык — ${langName}?`;
}

// Заявка на найм в читаемый для ИИ вид (только заполненные поля, с человекочитаемыми названиями).
function formDigest(form) {
  form = form || {};
  const val = k => { const v = form[k]; if (Array.isArray(v)) return v.join(', '); return v == null ? '' : String(v).trim(); };
  const lines = [];
  for (const f of R.REQUISITION_FIELDS) { const v = val(f.key); if (v) lines.push(`${f.label}: ${v}`); }
  // качества (traits) могут быть ключами — расшифруем
  if (form.traits) {
    let tr = form.traits; if (typeof tr === 'string') tr = tr.split(',').map(s => s.trim()).filter(Boolean);
    if (Array.isArray(tr) && tr.length) { const labels = tr.map(k => R.traitLabel ? R.traitLabel(k, 'ru') : k); lines.push('Главные качества: ' + labels.join(', ')); }
  }
  return lines.join('\n');
}

async function callClaude(system, user, maxTokens) {
  const key = getApiKey();
  if (!key) throw new Error('ANTHROPIC_API_KEY не задан');
  const r = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: { 'x-api-key': key, 'anthropic-version': '2023-06-01', 'content-type': 'application/json', 'anthropic-beta': 'prompt-caching-2024-07-31' },
    body: JSON.stringify({ model: MODEL, max_tokens: maxTokens || 3000, system, messages: [{ role: 'user', content: user }] }),
  });
  const d = await r.json();
  if (!r.ok) throw new Error('Anthropic: ' + ((d.error && d.error.message) || r.status));
  return (d.content || []).filter(b => b.type === 'text').map(b => b.text).join('').trim();
}

// Главный вызов. ctx: {form, lang, company, target, vacancyName}. Возвращает готовый текст объявления (2–3 варианта).
async function generateAd(ctx) {
  const lang = ['ru', 'pl', 'en'].includes(ctx.lang) ? ctx.lang : 'ru';
  const form = Object.assign({}, ctx.form || {});
  if (!form.position && ctx.vacancyName) form.position = ctx.vacancyName;
  const digest = formDigest(form) || ('Должность: ' + (form.position || ctx.vacancyName || '—'));
  const targetHint = ctx.target === 'executor' ? 'Подсказка пользователя: ориентир — хороший дуер (исполнитель), если должность это допускает.'
    : ctx.target === 'performer' ? 'Подсказка пользователя: ориентир — виннер (результатник).' : '';
  // KB кэшируем (статичен), затем — промт (тоже статичен на язык). Заявка — в user-сообщении.
  const system = [
    { type: 'text', text: KB, cache_control: { type: 'ephemeral', ttl: '1h' } },
    { type: 'text', text: promptFor(lang) },
  ];
  const user = `ЗАЯВКА НА НАЙМ:
${digest}
${ctx.company ? '\nКомпания: ' + ctx.company : ''}
${targetHint ? '\n' + targetHint : ''}

Составь объявление(я) строго по методологии и формату из инструкции. Используй КОНКРЕТИКУ из заявки (продукт, обязанности, сложности, условия), не общие штампы.`;
  return callClaude(system, user, 3200);
}

