// Промт кандидатского звонка по типу шага автоворонки.
function candidatePrompt(kind, vars) {
  const v = Object.assign({ agent_name: agentName(vars.language) }, vars);
  const tpl = kind === 'first' ? P1 : kind === 'afterResult' ? P2 : kind === 'afterTools' ? P4 : P2;
  return fill(tpl, v);
}
// Полная сборка референс-звонка (промт 3 + схема + резюме + первая реплика).
function referenceCall(vars) {
  const lang = ['ru', 'pl', 'en', 'de'].includes(vars.language) ? vars.language : 'ru';
  const v = Object.assign({ agent_name: agentName(lang) }, vars, { language: lang });
  return {
    systemPrompt: fill(P3, v),
    firstMessage: firstMessage('reference', lang === 'de' ? 'en' : lang, v),
    structuredDataSchema: REF_SCHEMA,
    summaryPrompt: (REF_SUMMARY[lang] || REF_SUMMARY.ru)(v.candidate || 'кандидат'),
  };
}

